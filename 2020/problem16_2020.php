            <div class="content">

                We want to determine the number of order $3$ in the symmetric group $S_6$. So from Theorem 1, we need only to consider the disjoint cycles structures such that the least common multiple of the lengths of the cycles is $3$. So the possible cycles are 
                \begin{align*}
                    (a_1a_2a_3),~~\text{and}~~(a_1a_2a_3)(a_4a_5a_6),
                \end{align*}
                So we need to determine how many elements are there of above type. 
                <ol>
                    <li>
                        <span class="def-head">The number of distinct elements of the form $(a_1a_2a_3):$</span> First of all note that the total number of elements of the above form is $6\times 5\times 4$, which is like $6$ choices for $a_1$, $5$ choices for $a_2$ and $4$ choices for $a_3$. But we need to rule out some of the elemnts from them as we know $(a_1a_2a_3) = (a_2a_3a_1) = (a_3a_1a_2)$. So we are counting three times more than we expect.  Thus the total number of elements of $3$ cycles are 
                        \begin{align*}
                            \dfrac{\binom{6}{3}\times 3!}{3} & = \dfrac{6\times 5 \times 4}{3} \\
                                                             & = 40.
                        \end{align*}
                    </li>
                    
                    <li>
                        <span class="def-head">The number of distinct elements of the form $(a_1a_2a_3)(a_4a_5a_6):$</span>
                        SImilar to the above computation without any restrictions we have $6!$ elements but again for both of the cycles we have $(a_1a_2a_3) = (a_2a_3a_1) = (a_3a_1a_2)$ and also we know that disjoint cycle commutes (Theorem-2) and hence the whole counting must be didived by $2$ and therefore, the total number of elements of the form $(a_1a_2a_3)(a_4a_5a_6)$ is 
                        \begin{align*}
                              & \dfrac{\frac{\binom{6}{3}\times 3!}{3} \times \frac{\binom{3}{3}\times 3!}{3}}{2} \\
                            = & \dfrac{40 \times 2}{2} \\
                            = & 40.
                        \end{align*} 
                    </li>
                </ol>
                 <figure class="img-box">
                    <img src="Images_2020/Problem16_2020.svg" alt="Total number of elements of order $3$ in $S_6$" style="width:70% ">
                    <!-- <figcaption>Counting total number of $3$ and $3-3$ cycles</figcaption> -->
                </figure><br>
                Thus, the total number of elements of order $3$ is $40+40=80$. 
