                <h3 class="definition"> Definition </h3>
                <span class="def-head">Symmetric group $S_n$: </span> The symmetric group $S_n$ is the set of all bijective functions from set $\{1,2,3,\cdots,n\}$ to itself. 
                
                <div class="theorem">
                    <strong>Theorem 1:</strong> The order of permutation of a finite set written in disjoint cycle form is the least common multiple of the lengths of the cycles. 
                </div>

                 <div class="theorem">
                    <strong>Theorem 2:</strong> Two disjoint cycle commutes. 
                </div>
                
                
